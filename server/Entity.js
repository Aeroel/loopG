export { Entity }
class Entity {
    x;
    y;
    width;
    height;
    color;
    constructor() {
    }
    setX(x) {
       this.x = x;
    }
    setY(y) {
      this.y = y;
    }
    setWidth(width) {
      this.width = width;
    }
    setHeight(height) {
      this.height = height;
    }
    setColor(color) {
      this.color = color;
    }
}